/**
 *p_add - calculates the sum of two numbers
 * @a: int 1
 * @b: int 2
 * Return: sum of int 1 and 2
**/

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - calculates the difference of numbers
 * @a: int 1
 * @b: int 2
 * Return: difference between a and b
**/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of two numbers
 * @a: int 1
 * @b: int 2
 * Return: product
**/

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides the first number by the second
 * @a: int 1
 * @b: int 2
 * Return: result
**/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Calculates the modulus
 * @a: int 1
 * @b: int 2
 * Return: result
**/

int op_mod(int a, int b)
{
return (a % b);
}
